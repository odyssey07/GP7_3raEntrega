    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        if (jtNombre.getText().isEmpty() || jtAño.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
            return;
        }
        if (materiaActual == null || materiaActual.getIdMateria() == 0) {
            materiaActual = new Materia();
            materiaActual.setNombre(jtNombre.getText());
            materiaActual.setAñoMateria(Integer.parseInt(jtAño.getText()));
            materiaActual.setActivo(jrEstado.isSelected());
            matData.guardarMateria(materiaActual);
        } else {
            materiaActual.setNombre(jtNombre.getText());
            materiaActual.setAñoMateria(Integer.parseInt(jtAño.getText()));
            materiaActual.setActivo(jrEstado.isSelected());
            matData.modificarMateria(materiaActual);
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

