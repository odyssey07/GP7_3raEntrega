    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here:
        if (jtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un código de materia.");
            return;
        }
        try {
            int idMateria = Integer.parseInt(jtCodigo.getText());
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar esta materia?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                matData.eliminarMateria(idMateria);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un número válido para el código.");
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

